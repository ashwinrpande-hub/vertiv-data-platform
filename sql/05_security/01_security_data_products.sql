GRANT USAGE  ON DATABASE VERTIV_PRODUCTS                          TO SHARE VERTIV_CUSTOMER_360_SHARE;
GRANT USAGE  ON SCHEMA   VERTIV_PRODUCTS.CUSTOMER_360             TO SHARE VERTIV_CUSTOMER_360_SHARE;


